function makeLookupTable(arr) {
  return arr
    .map(([idx, len, val])=> [idx, len, ('00000000000000000000000000000000' + val.toString(2)).slice(-len)])
    .sort((a,b)=>a[1] - b[1])
    .reduce((arr, p) => {
      let last = arr[arr.length - 1];
      if (!last || last.size !== p[1]) {
        last = {
          size: p[1],
        };
        arr.push(last);
      }
      last[p[2]] = p[0];

      return arr;
    }, []);
}

